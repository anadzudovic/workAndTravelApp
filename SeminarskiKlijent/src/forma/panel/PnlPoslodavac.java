    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        String vlasnik = txtVlasnik.getText();
        String email = txtEMail.getText();
        String naziv = txtNaziv.getText();
        String telefon = txtTelefon.getText();
        Grad g = (Grad) cbGrad.getSelectedItem();
        Poslodavac p = new Poslodavac(0, naziv, vlasnik, telefon, email, g);
        boolean val = validacija(vlasnik, email, naziv, telefon);
        if (val) {
            try {
                String poruka = Kontroler.getInstanca().unesiPoslodavca(p);
                JOptionPane.showMessageDialog(this, poruka);
                sesija.Sesija.getInstanca().getMapa().put("trenutniPoslodavac", p);
                System.out.println(p);
                if (((SwingUtilities.getWindowAncestor((FrmDialog) SwingUtilities.getWindowAncestor(this)))) instanceof GlavnaForma) {
                    return;
                }
                ((JDialog) SwingUtilities.getWindowAncestor(this)).dispose();
                (((FrmForma) SwingUtilities.getWindowAncestor((FrmDialog) SwingUtilities.getWindowAncestor(this)))).getPanel().popuniPodatke();
            } catch (NullPointerException ex) {
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Neuspesno cuvanje");
                Logger.getLogger(PnlPoslodavac.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

