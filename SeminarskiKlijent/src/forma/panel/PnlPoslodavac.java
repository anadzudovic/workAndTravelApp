    private void pripremiFormu(ModForme model) {
        ubaciGradove();
        switch (model) {
            case unos:
                btnSacuvaj.setVisible(true);

                break;
            case izmena:
                popuniPolja(sesija.Sesija.getInstanca().getMapa().get("trenutniPoslodavac"));
                btnSacuvaj.setVisible(false);

                break;
            case pregled:
                popuniPolja(sesija.Sesija.getInstanca().getMapa().get("trenutniPoslodavac"));
                btnSacuvaj.setVisible(false);

                txtEMail.setEditable(false);

                txtNaziv.setEditable(false);
                txtTelefon.setEditable(false);
                txtVlasnik.setEditable(false);
                cbGrad.setEditable(false);
                break;
        }
    }

    public ModForme getModel() {
        return model;
    }

    public void setModel(ModForme model) {
        this.model = model;
    }

    private void ubaciGradove() {
        try {
            cbGrad.removeAllItems();
            List<Grad> gradovi = kontroler.Kontroler.getInstanca().vratiSveGradove();
            for (Grad g : gradovi) {
                cbGrad.addItem(g);

            }
        } catch (IOException ex) {
            Logger.getLogger(PnlPoslodavac.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PnlPoslodavac.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    @Override
    public void popuniPolja(OpstaDomenskaKlasa odk) {
        Poslodavac p = (Poslodavac) odk;
        if (p != null) {
            txtEMail.setText(p.getElektronskaPosta());
            txtNaziv.setText(p.getNazivKompanije());
            txtTelefon.setText(p.getKontaktTelefon());
            txtVlasnik.setText(p.getImeIPrezimeVlasnika());
            cbGrad.setSelectedItem(p.getGrad());

        }
    }

    private boolean validacija(String vlasnik, String email, String naziv, String telefon) {
        if (vlasnik.isEmpty() || email.isEmpty() || naziv.isEmpty() || telefon.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti sve podatke!");
            return false;

        }

        if (!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Pogresan format za email adresu");
            return false;
        }
        return true;
    }

