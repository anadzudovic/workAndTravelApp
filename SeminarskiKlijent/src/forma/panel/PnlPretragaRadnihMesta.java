    private void btnVratiSveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVratiSveActionPerformed
        try {
            List<RadnoMesto> lista = kontroler.Kontroler.getInstanca().vratiSvaRM();
            model = new ModelTabeleRM();
            for (RadnoMesto s : lista) {
                for (RadnoMesto ss : sesija.Sesija.getInstanca().getRadnaMesta()) {
                    if (ss.equals(s)) {
                        s.setPreostaoBrojIzvrsioca(ss.getPreostaoBrojIzvrsioca());
                        break;
                    }
                }
                model.ubaci(s);

            }
            tblRM.setModel(model);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(PnlPretragaStudenata.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnVratiSveActionPerformed

