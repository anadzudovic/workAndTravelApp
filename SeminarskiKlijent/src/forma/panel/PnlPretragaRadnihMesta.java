    private void btnIzaberiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzaberiActionPerformed
        int index = tblRM.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Morate selektovati red u tabeli");
            return;
        }
        RadnoMesto p1 = model.getLista().get(index);
        if (p1.getDatumOd().before(new Date())) {
            JOptionPane.showMessageDialog(this, "Nije moguce kreirati angazovanje za ovo radno mesto(datumi su prosli)");
            return;

        }
        if (p1.getPreostaoBrojIzvrsioca() == 0) {
            JOptionPane.showMessageDialog(this, "Nije moguce kreirati angazovanje za ovo radno mesto(broj izvrsilaca=0)");
            return;
        }

        sesija.Sesija.getInstanca().getMapa().put("trenutnoRM", p1);
        System.out.println(p1);
        (((FrmForma) SwingUtilities.getWindowAncestor((FrmForma) SwingUtilities.getWindowAncestor(this)))).getPanel().popuniPodatke();
        ((FrmForma) SwingUtilities.getWindowAncestor(this)).dispose();
    }//GEN-LAST:event_btnIzaberiActionPerformed

    @Override
    public void popuniPodatke() {
        p = (Poslodavac) sesija.Sesija.getInstanca().getMapa().get("trenutniPoslodavac");
        if (p != null) {
            lblNaziv.setText("Naziv kompanije: " + p.getNazivKompanije());
            lblGrad.setText("Grad: " + p.getGrad().toString());
            lblKontaktTelefon.setText("Kontakt telefon: " + p.getKontaktTelefon());
        }
        if (sesija.Sesija.getInstanca().getMapa().get("trenutnoRM") != null) {

            model.ubaci((RadnoMesto) sesija.Sesija.getInstanca().getMapa().get("trenutnoRM"));
        }
    }

    public void pripremiFormu() {
        model = new ModelTabeleRM();
        tblRM.setModel(model);
        cbTipRM.removeAllItems();
        cbTipRM.addItem("Vrati sve");
        try {
            List<TipRadnogMesta> lista = kontroler.Kontroler.getInstanca().vratiSveTipoveRM();
            lista.sort(Comparator.comparing(TipRadnogMesta::getNazivTipaRadnogMesta));

            for (TipRadnogMesta rm : lista) {
                cbTipRM.addItem(rm);

            }
        } catch (Exception ex) {
            Logger.getLogger(PnlPoslodavac.class.getName()).log(Level.SEVERE, null, ex);

        }

    }


