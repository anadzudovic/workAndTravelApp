    private void btnOdjavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOdjavaActionPerformed
        ModelTabele model = (ModelTabele) tblKorisnici.getModel();
        int index = tblKorisnici.getSelectedRow();
        if (index != -1) {
            Agent a = model.izbaciRed(index);
            Kontroler.getInstanca().izbaciAgenta(a);
        } else {
            JOptionPane.showMessageDialog(this, "Morate izabrati red tabele!");
        }
    }//GEN-LAST:event_btnOdjavaActionPerformed

