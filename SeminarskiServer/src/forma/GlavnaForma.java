    private void srediTabelu() {
        ModelTabele model = new ModelTabele();
        tblKorisnici.setModel(model);
    }

    public void postaviStatus(String poruka, boolean signal) {

        lblStatus.setText(poruka);
        btnPokreni.setEnabled(signal);
        btnZaustavi.setEnabled(!signal);
        pnlKlijenti.setVisible(!signal);
        pnlKonfiguracija.setVisible(signal);

    }

    public void dodajPrijavljenogAgenta(Agent a) {
        ModelTabele model = (ModelTabele) tblKorisnici.getModel();
        model.dodajAgenta(a);
    }

    public void odjaviAgenta(Agent agent) {
       ModelTabele model = (ModelTabele) tblKorisnici.getModel();
        model.izbaciAgenta(agent);
    }

    public void popuniTabelu() {
       ModelTabele model = new ModelTabele();
       tblKorisnici.setModel(model);
    }

